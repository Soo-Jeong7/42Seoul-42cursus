#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t	i;
	char	c2;

	c2 = (char)c;
	i = 0;
	while (s[i])
	{
		if (s[i] == c2)
			return ((char *)s + i);
		i++;
	}
	if (s[i] == c2)
		return ((char *)s + i);
	return (0);
}
