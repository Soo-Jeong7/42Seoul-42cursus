#include "libft.h"

int	ft_isalpha(int c)
{
	unsigned char	c2;

	c2 = (unsigned char)c;
	if ((c2 >= 'A' && c2 <= 'Z')
		|| (c2 >= 'a' && c2 <= 'z'))
		return (1);
	return (0);
}
